class Solution {
    public boolean validPalindrome(String s) {
        int left = 0;
        int right = s.length() - 1;

        while (left < right) {
            if (s.charAt(left) != s.charAt(right)) {
                // Try deleting the character at the left pointer
                boolean skipLeft = isPalindrome(s, left + 1, right);
                
                // Try deleting the character at the right pointer
                boolean skipRight = isPalindrome(s, left, right - 1);
                
                return skipLeft || skipRight;
            }
            left++;
            right--;
        }

        return true;
    }

    private boolean isPalindrome(String s, int left, int right) {
        while (left < right) {
            if (s.charAt(left) != s.charAt(right)) {
                return false;
            }
            left++;
            right--;
        }
        return true;
    }
}


